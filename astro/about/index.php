<h2 style="text-align:center">科大天文爱好者协会简介</h1>
<br />
<p style="text-indent:2em;">
科大天文协会前身是工院的天文小组，但是一直没有发展起来，只是爱好者自己小规模进行天文观测活动。2004年，随着爱好者和观测次数的增加，有了成立天文协会的知识技术基础。2004年的两大天象（5月5日月全食、6月8日金星凌日）以及5月科技开放周天体物理中心开放实验室（天文台）开始使用，使准天文协会积累了一些组织活动的经验。 </p>

<p style="text-indent:2em;">
2004年3月30日，天文协会正式挂牌成立了，属于学校科技类社团，名义上属于安徽省天文学会。同年5月29日召开第一次会员大会。新生的天文协会遇到的困难还是比较多的，依靠各位骨干的努力工作得以维持。随着组织活动的增多以及新会员的崛起，目前协会已经脱离了成立之初的困顿，已经初具规模，并且必将会把活动组织得越来越好。 </p>

<p style="text-indent:2em;">
目前协会拥有许多装备：254mmDOB式反射镜一台，25*102双筒镜1台，102mm折射镜一台，150mm马卡式折反镜一台，还有一些会员拥有70、50mm双筒和小型折射、反射式望远镜。目前的活动分为校内和校外，校内依靠天文台，组织对月亮、太阳、大行星的观测以及天文科普活动，校外进行户外深空天体和流星雨、彗星的观测活动，并且在提高观测的科学目的、无线电观测流星、天文摄影等方面也正在积极探索。 </p>

<p style="text-indent:2em;">
天文协会是学校天文爱好者相互交流学习的舞台，也是学校天文爱好者面向校内师生以及社会大众的科普宣传的舞台，在这里你可以学到许多天文知识，认识全天星座，收揽许许多多深空天体，浪漫地细数流星，看国际空间站划过夜空……也可以展示你的社会活动能力，领导能力，演讲能力，为提高全民科学素养贡献力量。</p>

