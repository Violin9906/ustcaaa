<h2 style="text-align:center">天协网站简介与致谢</h2>
<br />
<p style="text-indent:2em">
天协网站最开始只是个小项目，目的是用在线的mysql数据库取代曾经放在社团办公室、总是丢的小本子，来管理社团的装备借用相关事务。网站从2018年7月开始构建，使用php开发。最开始的网站界面非常丑并且没有响应式设计（因为我的CSS没有学到家呀嘤嘤嘤），在手机上打开会出现排版错乱。感谢陈培学姐，让我了解到bootstrap。我花了一周的时间用bootstrap重新构建了前端页面（在此之前我还只会用php写写后端，几乎没有接触过前端开发），在由中科大Linux用户协会提供的服务器上短暂测试后，将网站上线阿里云（不是广告，阿里云的服务器是我自己出钱租的所以小可爱们可不可以捐助一下我呀嘤嘤嘤）。
</p>
<p style="text-indent:2em">
后来，网站的功能越来越完善，我们加入了观测成就系统，加入了会员VIP等级制度（误），并依托服务器上的wordpress进行信息发布和内容管理（这也是我初次接触ajax）。我们通过NASA的API加入了APOD的功能，可以欣赏美丽的天文图片（虽然加载速度实在慢）。为了给会员们快速提供天气信息，我们通过晴天钟的API加入了天气预报。这里要特别感谢晴天钟的维护者，他们的API一开始并不能通过AJAX进行跨域请求，在我提交issue后的第二天他们就进行了更新。现在我们的网站还在不断完善，我也在开发过程中不断的学习新的知识。
</p>
<p style="text-indent:2em">
最后，要感谢LUG的豆豆学长，以及一众在网站开发过程中给我建议和鼓励的朋友和同学们；感谢天协的朋友自始至终给我鼓励和信任；感谢忍受我在暑假期间早上7点起床敲机械键盘的室友们。
